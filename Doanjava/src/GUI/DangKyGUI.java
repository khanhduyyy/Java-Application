    public void them()
    {
        
        try { 
            String a=txtdk.getText();
            String b=txtmk.getText();
            for(int i=0;i<a.length();i++)
            {
                if(a.charAt(i)==' ')
                {
                    JOptionPane.showMessageDialog(rootPane,"Tên đăng nhập không được để khoảng trắng");
                    return;
                }
            }
            if(b.length()<6)
            {
                JOptionPane.showMessageDialog(rootPane,"Mật khẩu phải ít nhất 6 ký tự");
                return;
            }
                DangKyDTO tk = new DangKyDTO();
                //tk.setMaTK();
                //tk.setMaQuyen();
                tk.setTenDN(txtdk.getText());
                tk.setMatkhau(txtmk.getText());
                JOptionPane.showMessageDialog(this, tk1.addTaiKhoan(tk));
        } catch ( NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ"); 
        }
    }
